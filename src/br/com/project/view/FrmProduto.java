    private void tblProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdutoMouseClicked
        //Pega os dados da tabela e insere novamente nos textField para editar ou excluir.

        jTabbedPane2.setSelectedIndex(0);

        txtCodigo.setText(tblProduto.getValueAt(tblProduto.getSelectedRow(), 0).toString());
        txtDescricao.setText(tblProduto.getValueAt(tblProduto.getSelectedRow(), 1).toString());
        txtPrecoo.setText(tblProduto.getValueAt(tblProduto.getSelectedRow(), 2).toString());
        txtQtdEstoque.setText(tblProduto.getValueAt(tblProduto.getSelectedRow(), 3).toString());
        
        Fornecedor f = new Fornecedor();
        FornecedorDAO dao = new FornecedorDAO();
        f = dao.consultaPorNome(tblProduto.getValueAt(tblProduto.getSelectedRow(), 4).toString());
        
        cbFornecedor.removeAllItems();
        cbFornecedor.getModel().setSelectedItem(f);
        
    }//GEN-LAST:event_tblProdutoMouseClicked

    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        // Botão Salvar
        
        Produto obj = new Produto();

        obj.setDescricao(txtDescricao.getText());
        obj.setPreco(Double.parseDouble(txtPrecoo.getText()));
        obj.setQtdEstoque(Integer.parseInt(txtQtdEstoque.getText()));
       
        Fornecedor f = new Fornecedor();
        f = (Fornecedor) cbFornecedor.getSelectedItem();
        obj.setFornecedor(f);
        

        ProdutoDAO dao = new ProdutoDAO();
        dao.cadastrar(obj);

        new Utilitarios().LimparTela(jPanel2);
        
    }//GEN-LAST:event_BtnSalvarActionPerformed

