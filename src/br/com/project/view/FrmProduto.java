    public void listar() {

        //Metodo Listar na Tabela
        ProdutoDAO dao = new ProdutoDAO();
        List<Produto> lista = dao.listarProdutos();
        DefaultTableModel dados = (DefaultTableModel) tblProduto.getModel();
        dados.setNumRows(0);

        for (Produto c : lista) {
            dados.addRow(new Object[]{
                c.getIdProduto(),
                c.getDescricao(),
                c.getPreco(), 
                c.getQtdEstoque(),
                c.getFornecedor().getNome()
            });

        }

    }

